function solve(data, criteria) {
    let inputArr = JSON.parse(data);
    if (criteria == 'all') {
        print(inputArr);
    } else {
        let [typeOfCriteria, value] = criteria.split('-');
        let filtered = inputArr.filter(x => x[typeOfCriteria] == value);
        print(filtered);
    }

    function print(arr) {
        for (let i = 0; i < arr.length; i++) {
            let firstName = arr[i]["first_name"];
            let lastName = arr[i]["last_name"];
            let mail = arr[i]["email"];
            console.log(`${i}. ${firstName} ${lastName} - ${mail}`);
        }
    }

}


